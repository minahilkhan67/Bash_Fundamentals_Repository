#!/bin/bash

# For loops repeat code multiple times.
# They are useful for repeating tasks automatically.

# This loop counts from 1 to 5.

for i in {1..5}
do
    echo "Number: $i"

    # The variable changes each loop.
done

# How it works:
# The loop runs once for every number
# in the sequence.

